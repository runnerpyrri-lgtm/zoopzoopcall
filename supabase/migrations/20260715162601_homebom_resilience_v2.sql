-- 청약홈 호출 제한, 공개 스냅샷 이력, 문서 동기화의 실제 완료 상태를 영구 보존한다.
create table if not exists public.notice_upstream_state (
  source_key text primary key,
  retry_after timestamptz,
  last_error text,
  updated_at timestamptz not null default now()
);

alter table public.notice_upstream_state enable row level security;
comment on table public.notice_upstream_state is 'service role only upstream quota and retry state';

create table if not exists public.notice_public_snapshot_history (
  id bigint generated by default as identity primary key,
  feed_key text not null,
  notices jsonb not null,
  stats jsonb not null default '{}'::jsonb,
  verified_at timestamptz not null,
  created_at timestamptz not null default now(),
  constraint notice_public_snapshot_history_notices_array check (jsonb_typeof(notices) = 'array')
);

alter table public.notice_public_snapshot_history enable row level security;
comment on table public.notice_public_snapshot_history is 'service role only immutable validated feed history';
create index if not exists notice_public_snapshot_history_feed_verified_idx
  on public.notice_public_snapshot_history (feed_key, verified_at desc);

create table if not exists public.notice_sync_runs (
  id bigint generated by default as identity primary key,
  job_name text not null,
  status text not null check (status in ('succeeded', 'failed')),
  started_at timestamptz not null,
  finished_at timestamptz not null,
  result jsonb not null default '{}'::jsonb,
  error text,
  created_at timestamptz not null default now()
);

alter table public.notice_sync_runs enable row level security;
comment on table public.notice_sync_runs is 'service role only semantic scheduled sync results';
create index if not exists notice_sync_runs_job_started_idx
  on public.notice_sync_runs (job_name, started_at desc);

