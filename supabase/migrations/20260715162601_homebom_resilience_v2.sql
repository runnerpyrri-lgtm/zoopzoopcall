-- 고객 GET은 영구 스냅샷만 읽고, 업스트림 갱신은 인증된 시간별 작업만 수행한다.
do $$
declare
  existing_job_id bigint;
begin
  select jobid into existing_job_id from cron.job where jobname = 'homebom-notices-refresh';
  if existing_job_id is not null then perform cron.unschedule(existing_job_id); end if;
end $$;

select cron.schedule(
  'homebom-notices-refresh',
  '5 * * * *',
  $schedule$
  select net.http_post(
    url := 'https://neqjmxaneibobpedgsnl.supabase.co/functions/v1/notices?refresh=1',
    headers := jsonb_build_object(
      'content-type', 'application/json',
      'x-sync-token', coalesce((
        select decrypted_secret
        from vault.decrypted_secrets
        where name = 'homebom_notice_sync_token'
        limit 1
      ), '')
    ),
    body := '{}'::jsonb,
    timeout_milliseconds := 60000
  );
  $schedule$
);

-- 이력은 롤백·장애 분석에 충분한 30일만 보존한다.
do $$
declare
  existing_job_id bigint;
begin
  select jobid into existing_job_id from cron.job where jobname = 'homebom-snapshot-history-retention';
  if existing_job_id is not null then perform cron.unschedule(existing_job_id); end if;
end $$;

select cron.schedule(
  'homebom-snapshot-history-retention',
  '40 3 * * *',
  $$delete from public.notice_public_snapshot_history where created_at < now() - interval '30 days'$$
);
