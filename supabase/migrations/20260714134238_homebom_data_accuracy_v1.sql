-- 예약 함수 토큰은 Vault에 원문, 서비스 전용 테이블에 SHA-256만 보관한다.
create extension if not exists pgcrypto with schema extensions;

create table if not exists public.notice_sync_auth (
  singleton boolean primary key default true check (singleton),
  token_hash text not null check (length(token_hash) = 64),
  rotated_at timestamptz not null default now()
);

alter table public.notice_sync_auth enable row level security;
comment on table public.notice_sync_auth is 'service role only hash for scheduled official document sync authentication';

do $$
declare
  sync_token text := encode(extensions.gen_random_bytes(32), 'hex');
  existing_secret_id uuid;
begin
  select id into existing_secret_id
  from vault.secrets
  where name = 'homebom_notice_sync_token'
  limit 1;

  if existing_secret_id is null then
    perform vault.create_secret(sync_token, 'homebom_notice_sync_token', 'HomeBom official document sync token');
  else
    perform vault.update_secret(existing_secret_id, sync_token, 'homebom_notice_sync_token', 'HomeBom official document sync token');
  end if;

  insert into public.notice_sync_auth (singleton, token_hash, rotated_at)
  values (true, encode(extensions.digest(sync_token, 'sha256'), 'hex'), now())
  on conflict (singleton) do update
  set token_hash = excluded.token_hash,
      rotated_at = excluded.rotated_at;
end $$;

