-- 공식 상세 HTML·PDF의 변경 감지와 결정론적 추출 결과를 보존한다.
create table if not exists public.notice_document_cache (
  notice_key text primary key,
  notice_url text,
  document_url text,
  source_type text not null check (source_type in ('notice-html', 'notice-pdf', 'official-page', 'public-agency')),
  etag text,
  last_modified text,
  document_hash text,
  revision text,
  parsed_fields jsonb not null default '{}'::jsonb,
  provenance jsonb not null default '{}'::jsonb,
  conflicts jsonb not null default '[]'::jsonb,
  status text not null check (status in ('verified', 'single-official-source', 'conflict', 'not-provided', 'retrying')),
  fetched_at timestamptz not null,
  retry_after timestamptz,
  last_error text,
  updated_at timestamptz not null default now(),
  constraint notice_document_cache_fields_object check (jsonb_typeof(parsed_fields) = 'object'),
  constraint notice_document_cache_provenance_object check (jsonb_typeof(provenance) = 'object'),
  constraint notice_document_cache_conflicts_array check (jsonb_typeof(conflicts) = 'array')
);

alter table public.notice_document_cache enable row level security;
comment on table public.notice_document_cache is 'service role only official notice HTML and PDF extraction cache';
create index if not exists notice_document_cache_retry_after_idx
  on public.notice_document_cache (retry_after);

